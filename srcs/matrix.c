#include "fdf.h"

void			matrix_reset(t_matrix *m)
{
	m->m[0] = 0.0;
	m->m[1] = 0.0;
	m->m[2] = 0.0;
	m->m[3] = 0.0;
	m->m[4] = 0.0;
	m->m[5] = 0.0;
	m->m[6] = 0.0;
	m->m[7] = 0.0;
	m->m[8] = 0.0;
}

void			matrix_rot(t_matrix *m, double rot)
{
	t_matrix		rotm;

	matrix_reset(&rotm);
	rotm.m[0] = cos(rot);
	rotm.m[1] = sin(rot);
	rotm.m[3] = -sin(rot);
	rotm.m[4] = cos(rot);
	rotm.m[8] = 1;
	matrix_mult(m, &rotm);
}

void			matrix_mult(t_matrix *m1, t_matrix *m2)
{
	m1->m[0] = m1->m[0] * m2->m[0] + (m1->m[0] * m2->m[1]) + (m1->m[0] * m2->m[2]);
	m1->m[1] = m1->m[1] * m2->m[3] + (m1->m[1] * m2->m[4]) + (m1->m[1] * m2->m[5]);
	m1->m[2] = m1->m[2] * m2->m[6] + (m1->m[2] * m2->m[7]) + (m1->m[2] * m2->m[8]);
	m1->m[3] = m1->m[3] * m2->m[0] + (m1->m[3] * m2->m[1]) + (m1->m[3] * m2->m[2]);
	m1->m[4] = m1->m[4] * m2->m[3] + (m1->m[4] * m2->m[4]) + (m1->m[4] * m2->m[5]);
	m1->m[5] = m1->m[5] * m2->m[6] + (m1->m[5] * m2->m[7]) + (m1->m[5] * m2->m[8]);
	m1->m[6] = m1->m[6] * m2->m[0] + (m1->m[6] * m2->m[1]) + (m1->m[6] * m2->m[2]);
	m1->m[7] = m1->m[7] * m2->m[3] + (m1->m[7] * m2->m[4]) + (m1->m[7] * m2->m[5]);
	m1->m[8] = m1->m[8] * m2->m[6] + (m1->m[8] * m2->m[7]) + (m1->m[8] * m2->m[8]);
}
